#!/bin/bash
# -----------------------------------------------------------------
#  一键部署博客文章脚本 (macOS/Linux 版)
#  功能: 自动使用固定的提交信息完成 git add, commit, 和 push 操作。
#  使用方法:
#    1. 将此文件放置于你的博客项目根目录下。
#    2. 打开终端，进入项目根目录。
#    3. 运行 "chmod +x deploy.sh" (只需运行一次，使其可执行)。
#    4. 之后每次部署，只需运行 "./deploy.sh"。
# -----------------------------------------------------------------
clear
echo ""
echo "================================================"
echo "  正在准备快速部署你的博客文章..."
echo "================================================"
echo ""

echo "[步骤 1/3] 正在将所有文件更改添加到暂存区..."
git add .
echo "文件添加成功!"
echo ""

COMMIT_MESSAGE="更新博客内容"
echo "[步骤 2/3] 正在提交更改到本地仓库..."
git commit -m "$COMMIT_MESSAGE"
echo "提交成功! (使用信息: \"$COMMIT_MESSAGE\")"
echo ""

echo "[步骤 3/3] 正在将更改推送到 GitHub..."
git push
echo ""
echo "================================================"
echo "  推送完成! 你的网站即将开始自动构建。"
echo "================================================"
echo ""
